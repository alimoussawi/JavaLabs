package Lab1;
public class Task4 {
    public static void main(String[] args) {
        int a = 1;
        int b = 1;
        int c = 1;
        int d = 1;
        System.out.println("prefix increment : " + (++a));
        System.out.println("postfix increment : " + (b++));
        System.out.println("prefix decrement : " + (--c));
        System.out.println("postfix decrement : " + (d--));
    }
}
